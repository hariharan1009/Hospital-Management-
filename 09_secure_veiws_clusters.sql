TO ROLE HOSPITAL_ADMIN_ROLE;

CREATE OR REPLACE ROW ACCESS POLICY DEPARTMENT_POLICY
AS (DEPARTMENT STRING)
RETURNS BOOLEAN ->

CASE

    WHEN CURRENT_ROLE() = 'HOSPITAL_ADMIN_ROLE'
    THEN TRUE

    WHEN CURRENT_ROLE() = 'HOSPITAL_DOCTOR_ROLE'
         AND DEPARTMENT IN
         (
             'Cardiology',
             'Neurology'
         )
    THEN TRUE

    WHEN CURRENT_ROLE() = 'HOSPITAL_FINANCE_ROLE'
    THEN TRUE

    ELSE FALSE

END;

ALTER TABLE FACT_APPOINTMENT
ADD ROW ACCESS POLICY DEPARTMENT_POLICY
ON (DEPARTMENT);

ALTER TABLE FACT_APPOINTMENT
CLUSTER BY (APPT_DATE);

ALTER TABLE FACT_BILLING
CLUSTER BY (BILL_DATE);

