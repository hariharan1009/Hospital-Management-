USE WAREHOUSE HOSPITAL_MANAGEMENT_ANALYTICS;
USE DATABASE HOSPITAL_DATABASE;
USE SCHEMA HOSPITAL_MART;

CREATE OR REPLACE SECURE VIEW SV_PATIENTS AS
SELECT
    PATIENT_ID,
    FULL_NAME,
    PHONE,
    EMAIL,
    CITY,
    STATE,
    REGISTRATION_DATE
FROM DIM_PATIENT;

CREATE OR REPLACE SECURE VIEW SV_APPOINTMENTS AS
SELECT
    APPT_ID,
    APPT_DATE,
    PATIENT_ID,
    DOCTOR_ID,
    DOCTOR_NAME,
    DEPARTMENT,
    SLOT,
    STATUS
FROM FACT_APPOINTMENT;

CREATE OR REPLACE SECURE VIEW SV_BILLING AS
SELECT
    BILL_ID,
    BILL_DATE,
    PATIENT_ID,
    SERVICE_DESC,
    DEPARTMENT,
    NET_AMOUNT,
    PAYMENT_MODE,
    INSURER_NAME
FROM FACT_BILLING;

GRANT SELECT
