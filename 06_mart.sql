-- DIMENSION DYNAMIC TABLES

CREATE OR REPLACE DYNAMIC TABLE DIM_PATIENT
TARGET_LAG = '1 MINUTE'
WAREHOUSE = HOSPITAL_MANAGEMENT_ANALYTICS
AS
SELECT DISTINCT
    PATIENT_ID,
    FULL_NAME,
    DOB,
    GENDER,
    PHONE,
    EMAIL,
    CITY,
    STATE,
    REGISTRATION_DATE
FROM HOSPITAL_DATABASE.HOSPITAL_CORE.PATIENT_CORE;

-- create the dynmic doctor table
CREATE OR REPLACE DYNAMIC TABLE DIM_DOCTOR
TARGET_LAG = '1 MINUTE'
WAREHOUSE = HOSPITAL_MANAGEMENT_ANALYTICS
AS
SELECT DISTINCT
    DOCTOR_ID,
    DOCTOR_NAME,
    DEPARTMENT
FROM HOSPITAL_DATABASE.HOSPITAL_CORE.APPOINTMENT_CORE
WHERE DOCTOR_ID IS NOT NULL;

-- FACT DYNAMIC TABLES

CREATE OR REPLACE DYNAMIC TABLE FACT_APPOINTMENT
TARGET_LAG = '1 MINUTE'
WAREHOUSE = HOSPITAL_MANAGEMENT_ANALYTICS
AS
SELECT
    APPT_ID,
    APPT_DATE,
    PATIENT_ID,
    DOCTOR_ID,
    DOCTOR_NAME,
    DEPARTMENT,
    SLOT,
    STATUS
FROM HOSPITAL_DATABASE.HOSPITAL_CORE.APPOINTMENT_CORE;

-- create the DYNAMIC fact table for BILLING
CREATE OR REPLACE DYNAMIC TABLE FACT_BILLING
TARGET_LAG = '1 MINUTE'
WAREHOUSE = HOSPITAL_MANAGEMENT_ANALYTICS
AS
SELECT
    BILL_ID,
    BILL_DATE,
    PATIENT_ID,
    SERVICE_CODE,
    SERVICE_DESC,
    DEPARTMENT,
    GROSS_AMOUNT,
    DISCOUNT_AMOUNT,
    TAX_AMOUNT,
    NET_AMOUNT,
    PAYMENT_MODE,
    INSURER_NAME
FROM HOSPITAL_DATABASE.HOSPITAL_CORE.BILLING_CORE;

