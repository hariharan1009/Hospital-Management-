-- DEPARTMENT APPOINTMENT SUMMARY TABLE

CREATE OR REPLACE VIEW VW_DEPT_APPT_SUMMARY AS
SELECT
    DEPARTMENT,
    COUNT(*) AS TOTAL_APPOINTMENTS,

    SUM(
        CASE
            WHEN STATUS='Completed'
            THEN 1
            ELSE 0
        END
    ) AS COMPLETED_VISITS,

    SUM(
        CASE
            WHEN STATUS='No-Show'
            THEN 1
            ELSE 0
        END
    ) AS NO_SHOWS,

    ROUND(
        (
            SUM(
                CASE
                    WHEN STATUS='Completed'
                    THEN 1
                    ELSE 0
                END
            ) * 100.0
        ) / NULLIF(COUNT(*),0),
        2
    ) AS COMPLETION_RATE

FROM FACT_APPOINTMENT
GROUP BY DEPARTMENT;

-- DAILY OPD TREND

CREATE OR REPLACE VIEW VW_DAILY_OPD_TREND AS
SELECT
    APPT_DATE,

    COUNT(*) AS DAILY_VISITS,

    SUM(
        CASE
            WHEN STATUS='No-Show'
            THEN 1
            ELSE 0
        END
    ) AS NO_SHOWS,

    ROUND(
        COUNT(*) /
        NULLIF(COUNT(DISTINCT DOCTOR_ID),0),
        2
    ) AS AVG_APPOINTMENTS_PER_DOCTOR

FROM FACT_APPOINTMENT
GROUP BY APPT_DATE;


-- REVENUE BY DEPARTMENT

CREATE OR REPLACE VIEW VW_REVENUE_BY_DEPT AS
SELECT
    DEPARTMENT,

    SUM(GROSS_AMOUNT) AS GROSS_REVENUE,

    SUM(NET_AMOUNT) AS NET_REVENUE,

    ROUND(
        AVG(NET_AMOUNT),
        2
    ) AS AVG_BILL_VALUE

FROM FACT_BILLING
GROUP BY DEPARTMENT;


-- PATIENT UTILIZATION
CREATE OR REPLACE VIEW VW_PATIENT_UTILIZATION AS
SELECT

    P.PATIENT_ID,
    P.FULL_NAME,

    COUNT(DISTINCT A.APPT_ID) AS TOTAL_VISITS,

    MAX(A.APPT_DATE) AS LAST_VISIT_DATE,

    COALESCE(
        SUM(B.NET_AMOUNT),
        0
    ) AS LIFETIME_REVENUE

FROM DIM_PATIENT P
LEFT JOIN FACT_APPOINTMENT A
ON P.PATIENT_ID = A.PATIENT_ID
LEFT JOIN FACT_BILLING B
ON P.PATIENT_ID = B.PATIENT_ID
GROUP BY
P.PATIENT_ID,
P.FULL_NAME;


-- INSURANCE VS SELF PAY MIX

CREATE OR REPLACE VIEW VW_INSURANCE_MIX AS
SELECT

    CASE
        WHEN INSURER_NAME IS NULL
             OR INSURER_NAME = ''
        THEN 'SELF_PAY'
        ELSE 'INSURANCE'
    END AS PAYMENT_CATEGORY,

    COUNT(*) AS TOTAL_BILLS,

    SUM(NET_AMOUNT) AS REVENUE

FROM FACT_BILLING

GROUP BY
CASE
    WHEN INSURER_NAME IS NULL
         OR INSURER_NAME = ''
    THEN 'SELF_PAY'
    ELSE 'INSURANCE'
END;


